      /* ═══════════════════════════════════════════════
         BANKROLL HEALTH & ANALYTICS
         ROI       = Profit Net / Balanță Start  × 100  (randament fata de capitalul investit)
         Yield     = Profit Net / Total Mizat    × 100  (randament fata de cat s-a pariat efectiv)
         Max Drawdown = cea mai mare scadere (in RON) fata de un varf anterior
                        al soldului, parcurs cronologic prin biletele decise.
      ═══════════════════════════════════════════════ */
      function renderBankrollHealth(initialBudget, netProfit, totalStaked) {
        const roiEl      = document.getElementById('bh-roi');
        const yieldEl     = document.getElementById('bh-yield');
        const stakedEl    = document.getElementById('bh-staked');
        const drawdownEl  = document.getElementById('bh-drawdown');
        if (!roiEl || !yieldEl || !stakedEl || !drawdownEl) return;

        // ROI — fata de capitalul initial
        const roi = initialBudget > 0 ? (netProfit / initialBudget) * 100 : 0;
        roiEl.textContent = (roi >= 0 ? '' : '') + roi.toFixed(1) + '%';
        roiEl.className = 'bh-cell-val ' + (roi >= 0 ? 'pos' : 'neg');

        // Yield — fata de totalul mizat efectiv
        const yieldPct = totalStaked > 0 ? (netProfit / totalStaked) * 100 : 0;
        yieldEl.textContent = yieldPct.toFixed(1) + '%';
        yieldEl.className = 'bh-cell-val ' + (yieldPct >= 0 ? 'pos' : 'neg');

        // Total Mizat
        stakedEl.innerHTML = Math.round(totalStaked) + ' <span class="bh-unit">RON</span>';

        // Max Drawdown — calculat din seria cronologica de solduri
        const settledBets = getCurrentBets()
          .filter(b => b.status === 'win' || b.status === 'loss' || b.status === 'cashout')
          .sort((a, b) => a.id - b.id);

        let balance = initialBudget;
        let peak = initialBudget;
        let maxDrawdown = 0;
        settledBets.forEach(b => {
          if (b.status === 'win') balance += (b.stake * b.odds) - b.stake;
          else if (b.status === 'loss') balance -= b.stake;
          else if (b.status === 'cashout') balance += (b.cashoutAmount - b.stake);

          if (balance > peak) peak = balance;
          const drawdown = balance - peak; // valoare negativa sau 0
          if (drawdown < maxDrawdown) maxDrawdown = drawdown;
        });

        drawdownEl.innerHTML = Math.round(maxDrawdown) + ' <span class="bh-unit">RON</span>';
        drawdownEl.className = 'bh-cell-val ' + (maxDrawdown < 0 ? 'neg' : 'pos');
      }

