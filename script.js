      function renderProfitCalendar() {
        const grid = document.getElementById('cal-grid');
        const monthLabel = document.getElementById('cal-month-year');
        if (!grid || !monthLabel) return;

        const year = calDate.getFullYear();
        const month = calDate.getMonth();
        const monthName = calDate.toLocaleString(currentLang === 'ro' ? 'ro-RO' : 'en-GB', { month: 'long', year: 'numeric' });
        monthLabel.textContent = monthName.toUpperCase();
        let dailyProfits = {};
        getCurrentBets().forEach(b => {
          if (b.status === 'win' || b.status === 'loss' || b.status === 'cashout') {
            const bDate = new Date(b.date || parseInt(b.id)); 
            if (bDate.getFullYear() === year && bDate.getMonth() === month) {
              const day = bDate.getDate();
              let profit = 0;
              if (b.status === 'win') profit = (b.stake * b.odds) - b.stake;
              else if (b.status === 'loss') profit = -b.stake;
              else if (b.status === 'cashout') profit = b.cashoutAmount - b.stake;
              
              dailyProfits[day] = (dailyProfits[day] || 0) + profit;
            }
          }
        });
        grid.innerHTML = '';
        const firstDay = new Date(year, month, 1).getDay();
        const daysInMonth = new Date(year, month + 1, 0).getDate();
        let startOffset = firstDay === 0 ? 6 : firstDay - 1;
        for (let i = 0; i < startOffset; i++) {
          grid.innerHTML += `<div class="cal-cell empty"></div>`;
        }

        for (let d = 1; d <= daysInMonth; d++) {
          let pnl = dailyProfits[d];
          let cellClass = 'cal-cell';
          let valHtml = '';

          if (pnl !== undefined) {
            if (pnl > 0) {
              cellClass += ' cal-profit';
              valHtml = `<div class="cal-val">+${pnl.toFixed(0)}</div>`;
            } else if (pnl < 0) {
              cellClass += ' cal-loss';
              valHtml = `<div class="cal-val">${pnl.toFixed(0)}</div>`;
            } else {
              cellClass += ' cal-neutral';
              valHtml = `<div class="cal-val">0</div>`;
            }
          }

          grid.innerHTML += `
            <div class="${cellClass}">
              ${d}
              ${valHtml}
            </div>
          `;
        }
      }

