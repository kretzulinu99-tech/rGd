      function getCurrentBankValue() {
        let p = portfolios.find(x => x.id === currentPortfolioId);
        let initialBudget = p ? (parseFloat(p.budget) || 0) : 1000;
        let profitOffset = p ? (parseFloat(p.offset) || 0) : 0;
        let netProfit = 0;
        getCurrentBets().forEach(b => {
          if (b.status === 'win') netProfit += (b.stake * b.odds) - b.stake;
          else if (b.status === 'loss') netProfit -= b.stake;
          else if (b.status === 'cashout') netProfit += (b.cashoutAmount - b.stake);
        });
        let displayedProfit = netProfit - profitOffset;
        return initialBudget + displayedProfit;
      }

      function calculateInverseStakePct(totalOdds) {
        if (totalOdds <= 1) return 0;
        const pct = INVERSE_STAKE_K / totalOdds; // logica inversa: cota mare -> procent mic
        return Math.min(pct, INVERSE_STAKE_CAP);
      }

