      /* ═══════════════════════════════════════════════
         GESTIONAREA MIZELOR ÎN FUNCȚIE DE COTĂ
         (Odds-Based Stake Management / Inverse Staking System)

         Logica inversă: cu cât cota e mai mare, cu atât miza
         recomandată scade. Este un instrument de disciplină
         pentru limitarea expunerii la risc atunci când
         probabilitatea statistică de succes e mai mică
         (cote mari = risc mai mare = miză mai mică).

         Formulă: Miză% = K / CotăTotală
         K = constantă de calibrare (vezi INVERSE_STAKE_K)

         NOTĂ: Probabilitatea estimată (slider) e păstrată ca
         informație afișată și salvată pe bilet, dar NU mai
         intră în formula de calcul a mizei — sistemul de mize
         inverse se bazează STRICT pe cotă, fiind o metodă mai
         conservatoare și mai ușor de controlat emoțional decât
         un model probabilistic precum Criteriul Kelly.
      ═══════════════════════════════════════════════ */
      const INVERSE_STAKE_K   = 7;   // constanta de calibrare a formulei Miza% = K / Cota
